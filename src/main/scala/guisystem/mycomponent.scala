// Slider widget

class MySlider extends MyComponent
{

	var delay=3

	var roundto:Int= -1

	def GetValue=slider.getValue()

	def SetValue(value:Double,dotrigger:Boolean=true)
	{
		if(!dotrigger) trigger=false
		if(value < slider.getMin()) slider.setValue(slider.getMin())
		else if(value > slider.getMax()) slider.setValue(slider.getMax())
		else slider.setValue(value)
		if(hastext)
		{
			textfield.setText(""+value)
		}
		trigger=true
	}

	def GetMax=slider.getMax()

	def GetMin=slider.getMin()

	def GetMiddle=(GetMax-GetMin)/2

	var hbox:HBox=null

	var slider:Slider=null

	var textfield:TextField=null

	def hastext = ( roundto > 0 )

	def OwnCreateNode:Node=
	{	
		slider=new Slider()

		trigger=false

		if(HasAttribute("delay"))
		{
			delay=GI("delay",3)
		}

		if(HasAttribute("min"))
		{
			slider.setMin(GD("min",0.0))
		}
		if(HasAttribute("max"))
		{
			slider.setMax(GD("max",0.0))
		}
		if(HasAttribute("showticklabels"))
		{
			slider.setShowTickLabels(GB("showticklabels",false))
		}
		if(HasAttribute("showtickmarks"))
		{
			slider.setShowTickMarks(GB("showtickmarks",false))
		}
		if(HasAttribute("majortickunit"))
		{
			slider.setMajorTickUnit(GD("majortickunit",1.0))
		}
		if(HasAttribute("minortickcount"))
		{
			slider.setMinorTickCount(GI("minortickcount",1))
		}
		if(HasAttribute("blockincrement"))
		{
			slider.setBlockIncrement(GD("blockincrement",1.0))
		}

		if(HasAttribute("roundto"))
		{
			roundto=GI("roundto",1)
		}		

		hbox=new HBox(5)

		if(hastext)
		{
			textfield=new TextField()
			hbox.getChildren().add(textfield)

			textfield.setOnKeyPressed(new EventHandler[KeyEvent]
			{
				def handle(ev:KeyEvent)
				{
					if(ev.getCode().equals(KeyCode.ENTER))
					{						
						val text=textfield.getText()
						val value=DataUtils.ParseDouble(text,slider.getValue())
						SetValue(value,dotrigger=false)
						StoreValue(StringData(""+value))
						Fire("slider changed",""+value)
					}
				}
			})
		}

		hbox.getChildren().add(slider)

		val comp=this

		slider.valueProperty().addListener(new ChangeListener[Number]
		{
			def changed(ov:ObservableValue[_ <: Number],old_val:Number,new_val:Number)
			{
				if(trigger)
				{
					var new_val_str=new_val.toString()					

					var value=new_val_str.toDouble

					if(roundto>0)
					{
						val intvalue=value.toInt

						new_val_str=""+( intvalue - (intvalue % roundto) ).toDouble
					}					

					StoreValue(StringData(new_val_str))

					if(hastext)
					{
						MyActor.queuedExecutor ! ExecutionItem(client="MySlider.setText",code=new Runnable{def run{
							textfield.setText(new_val_str)
						}})
					}

					//Fire("slider changed",new_val_str)

					if(GetPath!=null)
					{
						MyActor.repeatActor ! SubmitEventRequest(GetHandler,
							MyEvent("slider changed",comp,new_val_str),delay)
					}
				}
			}
		})		

		val initvalue=GetInitialDoubleValue(GD("value",GetMiddle))

