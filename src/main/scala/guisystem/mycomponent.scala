// file chooser widget

class MyFileChooser() extends MyComponent
{

	var label:Label=null

	var path=""
	var dir=""

	def StoreData
	{
		StoreValue(MapData(Map("path"->StringData(path),"dir"->StringData(dir))))
	}

	def FromData
	{
		path=""
		dir=""
		val data=GetInitialValue
		if(data==null) return
		if(!data.isInstanceOf[MapData]) return
		val m=data.asInstanceOf[MapData].map
		if(m.contains("path")) path=m("path").asInstanceOf[StringData].value
		if(m.contains("dir")) dir=m("dir").asInstanceOf[StringData].value
	}

	def OwnCreateNode:Node=
	{
		val hbox=new HBox(3)

		node=hbox

		label=new Label()

		val button=new Button("...")

		button.setOnAction(new EventHandler[ActionEvent]
		{
			def handle(ev:ActionEvent)
			{				
				val fc=new FileChooser()

				val initial_dir=new java.io.File(dir)

				if(initial_dir.exists())
				{
					fc.setInitialDirectory(initial_dir)
				}

				val f=fc.showOpenDialog(new Stage())

				if(f!=null)
				{
					path=f.getPath()
					dir=f.getParent()

					label.setText(path)

					StoreData
				}
			}
		})

		hbox.getChildren().add(button)
		hbox.getChildren().add(label)
		
		FromData

		label.setText(path)

		node
	}

	def OwnReportPrintable(level:Int=0,buff:String=""):String="filechooser"
}

